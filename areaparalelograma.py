# Saudações #
print("Bem-vindo ao Calculador de Área de Paralelograma!")
# Obtenha os dados do usuário e armazene-os #
base = float(input("Insira o comprimento da base: "))
altura = float(input("Insira a altura da base: "))
# A área de um paralelograma é calculada multiplicando o valor da base pela altura #
area = base * altura
# Imprima o resultado #
print("A área do paralelograma é: ", area)